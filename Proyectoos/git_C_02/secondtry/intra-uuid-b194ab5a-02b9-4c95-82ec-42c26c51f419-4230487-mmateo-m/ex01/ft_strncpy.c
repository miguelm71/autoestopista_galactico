char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;
	int				e;

	i = 0;
	e = 0;
	while ((src[i] != '\0') && (i < n))
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
