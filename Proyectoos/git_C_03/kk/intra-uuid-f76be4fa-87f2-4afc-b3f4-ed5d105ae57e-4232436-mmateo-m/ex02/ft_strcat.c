char	*ft_strcat(char *dest, char *src)
{
	char	*d;
	char	*o;

	d = dest;
	o = src;
	while (*d != '\0')
		d++;
	while (*o != '\0')
	{
		*d = *o;
		d++;
		o++;
	}
	*d = '\0';
	return (dest);
}
